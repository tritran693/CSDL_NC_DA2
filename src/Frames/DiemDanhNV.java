    private void Tong() {
        DecimalFormat x=new DecimalFormat("###,###,###");
        int Tong=0;
        for (int i=0;i<TableDiemDanh.getRowCount();i++)
        {
            Tong+=Integer.parseInt(TableDiemDanh.getValueAt(i, 2).toString());
        }
        jLabelTongngay.setText("Tổng doanh số:"+x.format(Tong)+" "+"công");
    }

