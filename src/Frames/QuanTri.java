    private void tDSSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tDSSanPhamMouseClicked
        // TODO add your handling code here:
        int index = tDSSanPham.getSelectedRow();

        pMaSP.setText(tDSSanPham.getValueAt(index, 0).toString());
        pTenSP.setText(tDSSanPham.getValueAt(index, 1).toString());
        pNSX.setText(tDSSanPham.getValueAt(index, 2).toString());
        
        if(tDSSanPham.getValueAt(index, 3) == null){
            pMoTa.setText("");
        }else{
            pMoTa.setText(tDSSanPham.getValueAt(index, 3).toString());
        }

        String ngaySX, ngayHH, dateSX, dateHH;
        String[] dateArray;
        
        if(tDSSanPham.getValueAt(index, 4) == null){
            dateSX="";
        }else{
            dateSX = tDSSanPham.getValueAt(index, 4).toString();
        }
        
        if(tDSSanPham.getValueAt(index, 4) == null){
            dateHH="";
        }else{
            dateHH = tDSSanPham.getValueAt(index, 5).toString();
        }
        
        if(dateSX.isEmpty()){
            ngaySX="";
        }else{
            dateArray = dateSX.split("-");
            ngaySX = dateArray[2]+"-"+dateArray[1]+"-"+dateArray[0];
        }
        if(dateHH.isEmpty()){
            ngayHH="";
        }else{
            dateArray = dateHH.split("-");
            ngayHH = dateArray[2]+"-"+dateArray[1]+"-"+dateArray[0];
        }
        
        pNgaySX.setText(ngaySX);
        pNgayHH.setText(ngayHH);
        pSLTon.setText(tDSSanPham.getValueAt(index, 6).toString());
        pSLTrenQuay.setText(tDSSanPham.getValueAt(index, 7).toString());
        pGia.setText(tDSSanPham.getValueAt(index, 8).toString());
    }//GEN-LAST:event_tDSSanPhamMouseClicked

