    private void ThongKeBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThongKeBtn1ActionPerformed
        String year = namDropList1.getSelectedItem().toString();
        ArrayList<SoLuongThang> list = DBController.DBController.getSoLuongThang(year);

        //Bảng table
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tháng");
        model.addColumn("Tổng số lượng");

        //Biểu đồ
        DefaultCategoryDataset  dcd = new DefaultCategoryDataset();

        for(SoLuongThang dt: list){
            model.addRow(dt.toArray());
            dcd.setValue(dt.getTong(), "Số lượng", dt.getThang());
        }
        doanhThuThangTable1.setModel(model);

        JFreeChart jchart = ChartFactory.createBarChart3D("Số lượng hàng hóa được bán trong năm "+year, "Tháng", "Số lượng", dcd, PlotOrientation.VERTICAL, true, true, false);

        CategoryPlot plot = jchart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.PINK);

        ChartPanel chartPanel = new ChartPanel(jchart);

        reportPanel1.removeAll();
        reportPanel1.setLayout(new BorderLayout());
        reportPanel1.add(chartPanel, BorderLayout.NORTH);
        reportPanel1.updateUI();
    }//GEN-LAST:event_ThongKeBtn1ActionPerformed

