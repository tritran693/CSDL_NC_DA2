    private void ThongKeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThongKeBtnActionPerformed
        
        String year = namDropList.getSelectedItem().toString();
        ArrayList<DoanhThuThang> list = null;
        list = DBController.DBController.getDoanhThuThang(year);
        //Bảng table
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tháng");
        model.addColumn("Tổng doanh thu");
        
        //Biểu đồ
        DefaultCategoryDataset  dcd = new DefaultCategoryDataset();
        
        for(DoanhThuThang dt: list){
            model.addRow(dt.toArray());
            dcd.setValue(dt.getTongTien(), "Số tiền", dt.getThang());
        }
        doanhThuThangTable.setModel(model);
        
        JFreeChart jchart = ChartFactory.createBarChart3D("Doanh thu trong năm "+year, "Tháng", "Doanh thu (VNĐ)", dcd, PlotOrientation.VERTICAL, true, true, false);
        
        CategoryPlot plot = jchart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.BLUE);
        
        ChartPanel chartPanel = new ChartPanel(jchart);
        
        reportPanel.removeAll();
        reportPanel.setLayout(new BorderLayout());
        reportPanel.add(chartPanel, BorderLayout.NORTH);
        reportPanel.updateUI();
        
    }//GEN-LAST:event_ThongKeBtnActionPerformed

